/*遥控器数据处理宏*/
#define RC_CH_VALUE_OFFSET      ((uint16_t)1024)

/* ----------------------- 遥控器左/右开关值----------------------------- */
#define SWITCH_IS_UP ((uint16_t)1)   // 开关向上时的值
#define SWITCH_IS_MID ((uint16_t)3)  // 开关中间时的值
#define SWITCH_IS_DOWN ((uint16_t)2) // 开关向下时的值

// 用于遥控器数据读取,遥控器数据是一个大小为2的结构体数组，分为当前数据和上一次数据
#define CURRENT 0
#define LAST 1


//键盘按键状态
typedef union
{
    struct // 用于访问键盘状态
    {
        uint16_t w : 1;
        uint16_t s : 1;
        uint16_t d : 1;
        uint16_t a : 1;
        uint16_t shift : 1;
        uint16_t ctrl : 1;
        uint16_t q : 1;
        uint16_t e : 1;
        uint16_t r : 1;
        uint16_t f : 1;
        uint16_t g : 1;
        uint16_t z : 1;
        uint16_t x : 1;
        uint16_t c : 1;
        uint16_t v : 1;
        uint16_t b : 1;
    };
    uint16_t keys;
} Key_t;

//遥控器数据
#pragma pack(push, 1)
typedef struct
{
    //遥控器数据
    struct
    {
        int16_t Lrocker_x; // 左摇杆水平
        int16_t Lrocker_y; // 左摇杆竖直
        int16_t Rrocker_x; // 右摇杆水平
        int16_t Rrocker_y; // 右摇杆竖直
        int16_t dial;      // 侧边拨轮
        uint8_t Lswitch;  // 左侧开关
        uint8_t Rswitch; // 右侧开关
    } rc;
    //鼠标数据
    struct
    {
        int16_t x;
        int16_t y;
        int16_t z;
        uint8_t press_l;
        uint8_t press_r;
    } mouse;
    //键盘数据
    Key_t keyboard;
} RC_ctrl_t;

/**遥控器协议解析**/
